                      <div class="caption-mod-2 text-md-left">
                        <p><i>En Tunuhuire Chico, Puno, un hombre preserva y comparte cientos de años de historia y cultura.</i></p>
                        <p><b>Tiburcio</b> no escucha bien; un accidente hace tres años lo dejó un poco sordo. Ahora, hay que hablarle fuerte y al oído, en aymara de preferencia, porque no ha sacado sus audífonos hoy.</p>
                        <p>Comparte su desayuno con la Pacha Mama: un chorrito de jugo para ella un sorbo para él, siempre respetando a los Apus que protegen y proveen para la parcialidad de Tunuhuire chico, Centro Poblado de Ichu, en Puno. La importancia de hacer la distinción entre Tunuhuire Grande y Tunuhuire Chico, recuerda Tiburcio, viene de años de historia y separación de los espacios para los hijos del Inca Tunuhuire, la ladera más grande para el mayor y para el menor la más chica. Como esto, Tiburcio recuerda muchas cosas, recuerda todo. La memoria viva de su comunidad.</p>
                        
                        <p>Aunque es de día la luna se asoma a través de las ventanas del <b>Centro Ceremonial Inka Tunhuire</b>, un espacio que ha sido recuperado por la comunidad con el fin de preservar su cultura y la esperanza de atraer turismo. El Centro Ceremonial se presenta como el espacio clave a partir de cuál se construye la propuesta turística de Tunuhuire Chico por su historia, construcción y paisaje: a espaldas de Tiburcio, la luna, los cerros y el bosque de piedras; al frente de él, el lago Titicaca se extiende por kilómetros y se ve como el mar, no tiene final. </p>
                        <p>Tiburcio nunca deja de sonreír. Con ojos chiquitos, oscuros y expresivos, enmarcados en arrugas profundas. Sonríe con las muelas voladas, como dice él, y recuerda siglos de historia que él no vivió, pero le contaron. Soy un preguntón, dice, me interesa mucho saber de dónde venimos, de dónde somos. Y a punta de preguntas, a sus 73 años, parece tener todo el conocimiento colectivo de años y años de costumbres, leyendas, personas; patrimonio inmaterial de su comunidad que sobrevive y sigue encantando a quienes estén dispuestos a escuchar.</p>
                        <p>Tiene cuatro oficios a los que se dedica, alternando. Él los enumera. Primero al cultivo de plantas medicinales que crecen en huertos y en la naturaleza, en los cerros, frente al bosque de piedras, conviven con la comunidad. Tiburcio sabe manejarlas porque heredó el conocimiento ancestral de sus abuelos y sus padres y ha logrado mantenerlo vivo. Segundo a las artesanías y confección de vestuarios típicos del centro poblado de Ichu, la ropa que él lleva puesta son todas prendas que él mismo confeccionó y, confeccionará más tarde, los vestuarios para los bailes en la fiesta de San Pedro y San Pablo. Tercero a la agricultura, aprovechando los cultivos que crecen en la zona como la cebolla zanahoria, y en cuarto lugar, quizás su oficio más tradicional, a ser Yatiri. </p>
                        <p>Los Yatiri son los guías, maestros, de la comunidad aymara. A unos los poderes de la naturaleza los escogen, otros aprenden de sus antepasados, dice Tiburcio. Él aprendió de su papá, siguiéndolo y escuchándolo, prestando atención a sus enseñanzas como buen preguntón. Ahora, continúa con la tradición de su pueblo, venerando a la Pacha Mama, agradeciendo al sol, a los apus, a las piedras, al lago, al aire, al rayo, al arcoíris. Conserva el conocimiento, las historias, los secretos de la hoja de coca y la adivinanza de enfermedades y males. Mi hermano es mejor yatiri, igual.</p>
                        <p>Se saca los lentes para la cámara, porque son españoles, y vuelve a sonreír. ¿El secreto de su buen humor? Llevarse bien con sus vecinos y familia, saber trabajar en conjunto con respeto. Resalta lo importante de la igualdad entre hombres y mujeres; tienen igualdad de derechos, dice, que así se trabaja en la asociación. Hombres y mujeres juntos han limpiado y arreglado el camino que lleva a la parte alta del centro ceremonial; juntos están trabajando por sacar adelante la parcialidad y generar nuevas oportunidades para crecer.</p>
                        <p>Aunque no todo es mágico como sugeriría el ambiente en la parte alta de Tunuhuire Chico; los jóvenes han abandonado la esperanza de encontrar desarrollo y oportunidades ahí, y han migrado de a pocos a la ciudad de Puno. Ahora han quedado las personas mayores dedicadas a actividades productivas tradicionales, como la agricultura y la fabricación de zapatos tradicionales llamados ojotas. Pero no tiene que ser así. <b> asociación en Tunuhuire Chico ha reconocido en su espacio, el potencial de construir una propuesta turística que oferte su cultura y que cree oportunidades de desarrollo para todos y todas.</b></p>
