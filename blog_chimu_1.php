                    <div class="thumbnail"><img src="images/blog_chimu_1.jpg" alt="">
                      <div class="caption-mod-2 text-md-left">
                        <p><i>“Enrique observaba con resignación cómo muchas de las costumbres con las que creció ser perdían, al mismo tiempo que muchas de las especies que vivían en el lago y alrededores”, comenta Andrés Ugaz de cocinapar.org</i></p>
                        <p>“Si gracias al turismo logramos que el Timphu de Karachi se siga preparando, vale la pena el turismo”, sentencia Enrique Cuno Qanqi, 46 años, pescador, artesano y sobre todo, hijo del Lago Titicaca, quien entiende esta sopa a base de papas, chuño, muña y claro, karachi- pescado nativo- como un punto de referencia existencial, una columna vertebral donde enrosca su ser. Cuando lo conocí preparó el Timphu y mientras la sostenía en un pocillo de barro, estiró sus manos hacía mí y sin parpadear me dijo con la sonrisa nerviosa de quien sobrevivió de milagro: si uno sabe saborear el alma de esta sopa se le revela el tiempo recuperado.</p>
                        <p>Hace algunos años Enrique observaba con resignación cómo muchas de las costumbres con las que él creció se perdían al mismo tiempo que muchas de las especies que vivían en el lago y alrededores. Para él la vigencia de una sopa, de un tipo de amarre para su cordel, del ritual que su abuelo le enseñó celebrar antes de entrar al lago, significa la mejor forma de seguir siendo parte de un linaje que ahora sus hijos, gracias al turismo de base comunitaria, llevarán como un estandarte invisible y a la vez reconocible entre pares.</p>
                        <h4>Raices</h4>
                        <p>En las comunidades de Ichu alrededor del Lago Titicaca, 80 familias, 109 emprendedores directos y con un alcance de hasta 480 miembros de la comunidad, son el testimonio de un nuevo contrato social desde el turismo patrimonial comunitario integrando el Camino Inca. Es un proceso innovador impulsado por el PNUD y el proyecto Qhapaq Ñan- Sede Nacional del Ministerio de Cultura, que durante 3 años se dedicaron a registrar los puntos de sutura que sostienen el diálogo que va de una generación a otra, de hombres y mujeres que nunca se han separado de la naturaleza, que se saben fragmentos de ella y que toda su sabiduría procede de esa evidencia.</p>
                        <p>Es un registro que parece prefiguraba el tiempo que vivimos para lograr todo lo que se buscará cuando los viajes se retomen. Un registro con las familias, las autoridades, los artistas, poetas, artesanos, músicos, curanderos, mayordomos, cocineros, cocineras, pescadores y campesinos que han narrado lo que conocen de su arte y muchos oficios, de sus celebraciones, creencias, sueños y miedos. En el camino, entre capacitaciones, implementación de infraestructura validada por ellos mismos, dentro de los resultados más valorados está la integración de las parcialidades de Tunuhuiri, Jayujayuni y Chimu, la recuperación de la confianza como un valor intransferible, la formalización como estrategia empresarial y del enorme capital en la reciprocidad, como la banca de estas pequeñas sociedades.</p>
                        <p>Visitar las comunidades de Ichu, a tan solo diez minutos de la ciudad de Puno, es transitar aguas del turquesa al azul, es suspenderse en geologías lunares, es respirar aire crudo y glacial, es conocer gente que no se dedica a dar lecciones de ética y vínculos respetuosos con su entorno y sus antepasados, sino a vivir ética y respetuosamente con su entorno y pasado común.</p>
                        <p>Pero también es constatar verdades esenciales en las diferencias y en el enorme valor de los detalles cotidianos, del valor de la palabra dada y del vínculo que nunca debimos perder con el entorno. De que el yo no se diluye en otra geografía humana, se colorea, se modela y nutre con una sopa por ejemplo, para regresar a casa y lograr la mejor versión de nosotros mismos y de nuestros nuevos vínculos, en los que si no hay reciprocidad, mejor no insistan.</p>
